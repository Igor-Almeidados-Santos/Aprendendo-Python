a = 3 #Atribuição de valor a uma variável;
print(a)
a = a + 7 #Atribuição de soma da própria variável com outro valor;
print(a)
a += 2 #Atribuição de soma da variável com outro valor;
print(a)
a -= 1 #Atribuição de subtração da variável com outro valor;
print(a)
a *= 20 #Atribuição de multiplicação da variável com outro valor;
print(a)
a /= 2 #Atrubuição de divisão da variável com outro valor;
print(a)
a %= 7 #Atribução do resto da divisão da variável com outro valor;
print(a)
a **= 5 #Atribuição da potenciação da variável com outro valor;
print(a)
a //= 256 #Atribuição da divisão inteira da variável com outro valor.
print(a)