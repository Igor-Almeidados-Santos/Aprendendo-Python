#Operador "e" : É verdadeiro se todas as operações forem verdadeiras
#Operador "ou" : É Falso quando todas as operações são falsas
#Operador "xor(!=)" É verdadeiro quando todos os operadores são diferentes

#Tabela verdade "e"
print(True and True)
print(True and False)
print(False and True)
print(False and False)

#Tabela verdade "ou"
print(True or True)
print(True or False)
print(False or True)
print(False or False)

#Tabela verdade "xor"
print(True != True)
print(True != False)
print(False != True)
print(False != False)

#operador de negação
print(not True)
print(not False)
print(not 0)
print(not 1)
print(not not -1)
print(not not True)